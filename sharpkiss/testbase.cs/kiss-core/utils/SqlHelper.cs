        public enum Version
        {
            Unknown = -1,
            SQLServer2000 = 1,
            SQLServer2005 = 2,
            SQLServer2008 = 3
        }

        const string SQL_GETVERSION = "SELECT SUBSTRING(CONVERT(varchar,SERVERPROPERTY('productversion')),1,PATINDEX('.',CONVERT(varchar,SERVERPROPERTY('productversion')))+2) AS Version";

        /// <summary>
        /// get sql server main version
        /// </summary>
        /// <param name="connectionstring"></param>
        /// <returns></returns>
        public static Version GetVersion(string connectionstring)
        {
            float version = float.Parse(ExecuteScalar(connectionstring, CommandType.Text, SQL_GETVERSION).ToString().Replace(".", string.Empty));

            if ((version >= 8) && (version < 9)) return Version.SQLServer2000;
            if ((version >= 9) && (version < 10)) return Version.SQLServer2005;
            if ((version >= 10) && (version < 11)) return Version.SQLServer2008;

            return Version.Unknown;
        }

        /// <summary>
        /// Prepare a command for execution
        /// </summary>
        /// <param name="cmd">SqlCommand object</param>
        /// <param name="conn">SqlConnection object</param>
        /// <param name="trans">SqlTransaction object</param>
        /// <param name="cmdType">Cmd type e.g. stored procedure or text</param>
        /// <param name="cmdText">Command text, e.g. Select * from Products</param>
        /// <param name="cmdParms">SqlParameters to use in the command</param>
        private static void PrepareCommand(SqlCommand cmd, SqlConnection conn, SqlTransaction trans, CommandType cmdType, string cmdText, SqlParameter[] cmdParms)
        {
            if (conn.State != ConnectionState.Open)
                conn.Open();

            cmd.Connection = conn;
            cmd.CommandText = cmdText;

            if (trans != null)
                cmd.Transaction = trans;

            cmd.CommandType = cmdType;

            if (cmdParms != null)
            {
                foreach (SqlParameter parm in cmdParms)
                    cmd.Parameters.Add(parm);
            }
        }
    }
